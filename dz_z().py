text = input("Введіть текст ,та помітку в ( ), на виході отримаєте текст без поміток: ")  # Введіть тескт
a = text.find("(")  # находимо індекс перших дужок
b = text.find(")")  # індекс другої дужки
c = (text[a:b+1])  # створюєм змінну яка міститиме текст в дужках з дужками
v = text.replace(c, "")  # Копіюєм в нову змінну наш текст но без дужок та тексту в них
print(v)

def avtodzz():
    v = float(input("Витрачання палива автомобілем за 100км:"))
    v_per1 = v / 100  # рахуємо вартість 1 літру
    print("Сьогоднішня ціна 1 л палива = 51.03грн ")
    km = float(input("Скільки кілометрів треба здолати: "))  # запитуємо яку відстань вони збираються проїхати
    purchase = (v_per1 * km) * 51.03  # рахуємо вартість витрат
    x = f"Вартість поїздки в {km}км, буде коштувати = {round(purchase, 2)}"  # результат
    return x


answer = avtodzz()
print(answer)

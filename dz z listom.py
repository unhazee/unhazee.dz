b = list()  # створили пустий ліст
while True:
    a = input("Ваше число")  # створюєм перемінну в яку вводимо числа
    if a == "sum":  # Якщо вводять sum то сумуєм числа в лісті
        print(sum(b))
        break
    try:
        b.append(float(a))  # добавляєм числа в ліст b

    except Exception:
        print("Введіть будь ласка число , або sum, а не текст")  # якщо вводять не цифри просить ввести цифру або sum
    print(b)
